public class TransferPackage {

    private String field1;
    private int field2;

    public TransferPackage(String field1, int field2) {
        this.field1 = field1;
        this.field2 = field2;
    }

    public TransferPackage() {
    }

    public String getField1() {
        return field1;
    }

    public int getField2() {
        return field2;
    }
}
